    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Metodos met = new Metodos();
        
        Funcionario f = met.busca(cpf);
        if (f.getFuncao().equals("Vendedor")) {
            new MenuFuncionario(cpf).show();
        } else {
            new MenuGerente(cpf).show();
        }
        this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Cadastro cadEnt = new Cadastro();
        int op = JOptionPane.showConfirmDialog(null, "Realmente deseja efetuar a entrega e adicionar pedido?");
        if(op==0){
            if(txtNome.getText().equals("") || txtCpf.getText().equals("") || txtRG.getText().equals("") || txtFone.getText().equals("") || txtEnd.getText().equals("")){
                JOptionPane.showMessageDialog(null, "PREENCHA TODOS OS CAMPOS", "ERRO", JOptionPane.ERROR_MESSAGE);
            }else{
                cadEnt.cadastrarEntrega(new Entrega(txtNome.getText(), txtCpf.getText(), txtRG.getText(), txtFone.getText(),txtEnd.getText()));
            }
            dispose();
            try {
                new CadPedido(cpf, txtCpf.getText()).show();
            } catch (SQLException ex) {
                Logger.getLogger(TelaEntrega.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

