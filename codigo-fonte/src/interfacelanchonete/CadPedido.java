    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Metodos met = new Metodos();
        Funcionario f = met.busca(cpf);
        if (f.getFuncao().equals("Vendedor")) {
            new MenuFuncionario(cpf).show();
        } else {
            new MenuGerente(cpf).show();
        }
        this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       DefaultTableModel origem = (DefaultTableModel)tabelaL.getModel();
       DefaultTableModel destino = (DefaultTableModel)tabelaP.getModel();
       Object[] obj = {origem.getValueAt(tabelaL.getSelectedRow(), 0),origem.getValueAt(tabelaL.getSelectedRow(), 1),origem.getValueAt(tabelaL.getSelectedRow(), 2),origem.getValueAt(tabelaL.getSelectedRow(), 3)};
       destino.addRow(obj);
    }//GEN-LAST:event_jButton1ActionPerformed

