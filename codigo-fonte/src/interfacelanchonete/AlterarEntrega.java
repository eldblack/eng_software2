    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
                ConexaoSQlite conSQLite = new ConexaoSQlite();
                conSQLite.conectar();
		PreparedStatement preparedStatement = null;
		String sql;
		
			sql = "UPDATE tbl_entrega"
				+ " SET "
				+ " endereco = ?"
				+ "	WHERE cpf = ?";
				
		
		try {
			preparedStatement = conSQLite.criarPreparedStatement(sql);
			preparedStatement.setString(1, end.getText());
			preparedStatement.setString(2, buscaCpf.getText());
			
			preparedStatement.executeUpdate();
			JOptionPane.showMessageDialog(null, "ENDERE√áO ALTERADO", "ALTERAR", JOptionPane.INFORMATION_MESSAGE);
		}catch(SQLException err) {
			
			JOptionPane.showMessageDialog(null, "ERRO NA ALTERA√á√ÉO", "ALTERAR", JOptionPane.ERROR_MESSAGE);

		}finally {
			
			try {
				preparedStatement.close();
				conSQLite.desconectar();
			}catch(SQLException err) {
				JOptionPane.showMessageDialog(null, "ERRO NA ALTERA√á√ÉO", "ALTERAR", JOptionPane.ERROR_MESSAGE);

			}
		}
    }//GEN-LAST:event_jButton4ActionPerformed

